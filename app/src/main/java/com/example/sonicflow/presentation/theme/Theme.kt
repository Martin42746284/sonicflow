        @Composable get() = if (isSystemInDarkTheme()) DarkProgressBar else ProgressBar

    val controlActive: androidx.compose.ui.graphics.Color
        @Composable get() = if (isSystemInDarkTheme()) DarkPlayerControlActive else PlayerControlActive

    val controlInactive: androidx.compose.ui.graphics.Color
        @Composable get() = if (isSystemInDarkTheme()) DarkPlayerControlInactive else PlayerControlInactive
}
